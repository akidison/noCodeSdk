// Pushwoosh NoCode — Code Generators (JSON + Swift)

// === JSON Generator ===

function generateJSON(workspace) {
    const topBlocks = workspace.getTopBlocks(true);
    const config = { version: 1 };

    for (const block of topBlocks) {
        if (block.type === 'pw_on_app_start') {
            config.onAppStart = getActionsFromStatement(block, 'ACTIONS');
        }
    }

    return config;
}

function getActionsFromStatement(parentBlock, statementName) {
    const actions = [];
    let block = parentBlock.getInputTargetBlock(statementName);

    while (block) {
        const action = blockToAction(block);
        if (action) actions.push(action);
        block = block.getNextBlock();
    }

    return actions;
}

function blockToAction(block) {
    switch (block.type) {
        case 'pw_register_push':
            return { type: 'registerForPushNotifications' };

        case 'pw_set_user_id':
            return { type: 'setUserId', value: block.getFieldValue('VALUE') };

        case 'pw_set_email':
            return { type: 'setEmail', value: block.getFieldValue('VALUE') };

        case 'pw_set_tag':
            return {
                type: 'setTags',
                tags: { [block.getFieldValue('KEY')]: block.getFieldValue('VALUE') }
            };

        case 'pw_increment_tag':
            return {
                type: 'incrementTag',
                key: block.getFieldValue('KEY'),
                value: Number(block.getFieldValue('VALUE'))
            };

        case 'pw_set_language':
            return { type: 'setLanguage', value: block.getFieldValue('VALUE') };

        case 'pw_start_communication':
            return { type: 'startCommunication' };

        case 'pw_stop_communication':
            return { type: 'stopCommunication' };

        case 'pw_set_reverse_proxy':
            return { type: 'setReverseProxy', value: block.getFieldValue('VALUE') };

        default:
            return null;
    }
}

// === Swift Generator ===

function generateSwift(workspace) {
    const config = generateJSON(workspace);
    const lines = [];

    lines.push('// AppDelegate.swift');
    lines.push('// Generated by Pushwoosh NoCode Builder');
    lines.push('');
    lines.push('import PushwooshFramework');
    lines.push('');
    lines.push('func application(_ application: UIApplication,');
    lines.push('                 didFinishLaunchingWithOptions launchOptions: [UIApplication.LaunchOptionsKey: Any]?) -> Bool {');

    if (config.onAppStart) {
        for (const action of config.onAppStart) {
            const swift = actionToSwift(action);
            if (swift) lines.push('    ' + swift);
        }
    }

    lines.push('    return true');
    lines.push('}');

    return lines.join('\n');
}

function actionToSwift(action) {
    switch (action.type) {
        case 'registerForPushNotifications':
            return 'Pushwoosh.configure.registerForPushNotifications()';

        case 'setUserId':
            return `Pushwoosh.configure.setUserId("${action.value}")`;

        case 'setEmail':
            return `Pushwoosh.configure.setEmail("${action.value}")`;

        case 'setTags': {
            const entries = Object.entries(action.tags)
                .map(([k, v]) => `"${k}": "${v}"`)
                .join(', ');
            return `Pushwoosh.configure.setTags([${entries}])`;
        }

        case 'incrementTag':
            return `Pushwoosh.configure.setTags(["${action.key}": PWTagsBuilder.incrementalTag(withInteger: ${action.value})])`;

        case 'setLanguage':
            return `Pushwoosh.configure.setLanguage("${action.value}")`;

        case 'startCommunication':
            return 'Pushwoosh.configure.startServerCommunication()';

        case 'stopCommunication':
            return 'Pushwoosh.configure.stopServerCommunication()';

        case 'setReverseProxy':
            return `Pushwoosh.configure.setReverseProxy("${action.value}")`;

        default:
            return null;
    }
}
